package basics;

public class Operators {

	public static void main(String[] args) {
		// operators:+,-,/,%,*,,++,--,.......Arthematic operators
		// Relational operators......>,<,==,<=,>=,!=,=
		int i = 10;
		int j = 20;
		int b = 20;
		int a = i + j;
		System.out.println(a);// 30
		System.out.println(i - j);// -10
		System.out.println(i * j);// 200
		System.out.println(j / b);// 1
		System.out.println(j % b);// 0
		i++;// i=i+1
		System.out.println(i);// 11
		i--;// i=i-1
		System.out.println(i);
		// Relational operators......>,<,==,<=,>=,!=,=
		
		//boolean true false
		
		System.out.println(i>j);//false
		
		
		
		System.out.println(b==j);
		System.out.println(i<=j);
		
		//==...compare the two things are same or not
		
		//=...assign the right hand sidevalue in to the left hand side value
		
		
	}

}
